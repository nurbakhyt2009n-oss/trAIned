_BY_ID = {ex["id"]: ex for ex in EXERCISES}

# порядок категорий
CATEGORIES = ["Push", "Pull", "Legs", "Core"]

# оборудование (для фильтра на фронтенде)
EQUIPMENT = [
    {"id": "bodyweight", "label": "Своё тело"},
    {"id": "dumbbells",  "label": "Гантели"},
    {"id": "gym",        "label": "Зал / штанга"},
    {"id": "band",       "label": "Резина / жгут"},
]


def get_exercise(exercise_id):
    """Найти упражнение по id (или None)."""
    return _BY_ID.get(exercise_id)


def get_grouped():
    """Библиотека, сгруппированная по категориям — для фронтенда."""
    grouped = []
    for cat in CATEGORIES:
        items = [ex for ex in EXERCISES if ex["category"] == cat]
        grouped.append({"category": cat, "exercises": items})
    return grouped


def get_equipment():
    """Список типов оборудования с русскими подписями."""
    return EQUIPMENT


def suggest_progression(ex_type, last):
    """Подсказка по прогрессивной перегрузке на основе последнего подхода."""
    reps = last.get("reps") or 0
    weight = last.get("weight") or 0

    if ex_type == "weight":
        if reps >= 12:
            return f"Добавь вес: +2.5 кг ({weight + 2.5} кг) и начни заново с 8 повторов."
        return f"Прибавь повтор: {reps + 1} × {weight} кг."
    else:
        return f"Цель — на 1 повтор больше: {reps + 1} повторов."